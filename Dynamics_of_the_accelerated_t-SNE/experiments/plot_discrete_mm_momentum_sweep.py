#!/usr/bin/env python3
"""
Plot EE-stage endpoints and final embedding-stage endpoints for the
discrete MM momentum sweep.

Input:
  - discrete_mm_momentum_sweep.npz
    generated by compute_discrete_mm_momentum_sweep.py

Outputs:
  - ee_stage_end_discrete_mm_momentum_sweep.pdf
  - embedding_stage_end_discrete_mm_momentum_sweep.pdf

Default behavior:
  - EE panels use per-panel limits, because the EE-stage scale can differ
    substantially across momentum coefficients.
  - Embedding panels use global limits, because the final embeddings are usually
    comparable after the common original t-SNE embedding stage.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import matplotlib.pyplot as plt


DEFAULT_MOMENTUMS = [0.0, 0.1, 0.3, 0.5, 0.7, 0.9]


def parse_args():
    """
    Parse command-line options for momentum-sweep plotting.

    - inputs:
        - CLI arguments from the current process.
    - outputs:
        - argparse.Namespace with plotting and layout options.
    - process:
        - Define parser flags for paths, scales, styles, and render settings.
    """
    parser = argparse.ArgumentParser(
        description="Plot EE and final embedding results for discrete MM momentum sweep."
    )
    parser.add_argument(
        "--npz",
        default="outputs/discrete_mm_momentum_sweep/discrete_mm_momentum_sweep.npz",
        help="Path to discrete_mm_momentum_sweep.npz.",
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Output directory. If omitted, uses the directory containing --npz.",
    )
    parser.add_argument(
        "--momentums",
        nargs="+",
        type=float,
        default=None,
        help="Momentum coefficients to plot. Default: read from npz.",
    )
    parser.add_argument(
        "--figure-size",
        nargs=2,
        type=float,
        default=[12.0, 7.0],
        metavar=("WIDTH", "HEIGHT"),
    )
    parser.add_argument("--point-size", type=float, default=8.0)
    parser.add_argument("--alpha", type=float, default=0.75)
    parser.add_argument("--dpi", type=int, default=300)

    parser.add_argument(
        "--ee-scale",
        choices=["panel", "global"],
        default="panel",
        help="Axis scaling for the EE-stage figure. Default: panel.",
    )
    parser.add_argument(
        "--embedding-scale",
        choices=["panel", "global"],
        default="global",
        help="Axis scaling for the embedding-stage figure. Default: global.",
    )
    parser.add_argument(
        "--equal-each-panel",
        action="store_true",
        help="Use equal x/y aspect ratio within each panel.",
    )
    parser.add_argument(
        "--limit-padding-ratio",
        type=float,
        default=0.06,
        help="Padding ratio for panel/global limits.",
    )
    parser.add_argument(
        "--legend",
        choices=["last", "outside", "none"],
        default="last",
        help="Legend placement.",
    )
    return parser.parse_args()


def momentum_label(momentum: float) -> str:
    """
    Convert momentum value into key-safe label text.

    - inputs:
        - Momentum coefficient as float.
    - outputs:
        - Label string matching saved npz key naming.
    - process:
        - Format numeric value and replace special characters.
    """
    return f"m{momentum:.1f}".replace(".", "p").replace("-", "neg")


def panel_title(momentum: float, stage: str) -> str:
    """
    Build panel title string for one momentum/stage pair.

    - inputs:
        - Momentum value and stage name.
    - outputs:
        - Human-readable subplot title.
    - process:
        - Compose GD/MM label text and prepend stage-specific prefix.
    """
    if abs(momentum) < 1e-12:
        name = "GD / momentum=0.0"
    else:
        name = f"MM / momentum={momentum:.1f}"
    if stage == "ee":
        return f"EE: {name}"
    if stage == "embedding":
        return f"Embedding: {name}"
    return name


def get_momentums(data, requested):
    """
    Resolve momentum list from CLI request or npz metadata.

    - inputs:
        - Loaded npz data object and optional requested momentum list.
    - outputs:
        - List of momentum values to plot.
    - process:
        - Prefer requested values, then npz ee_momentums, then built-in defaults.
    """
    if requested is not None:
        return [float(x) for x in requested]
    if "ee_momentums" in data:
        return [float(x) for x in np.asarray(data["ee_momentums"]).ravel()]
    return DEFAULT_MOMENTUMS


def get_array_for_momentum(data, momentum: float, suffix: str):
    """
    Load one stored array for a specific momentum and suffix.

    - inputs:
        - npz data object, momentum value, and key suffix.
    - outputs:
        - ndarray corresponding to the selected key.
    - process:
        - Build key name, validate presence, and return array (or raise helpful error).
    """
    label = momentum_label(momentum)
    key = f"ee_momentum_{label}_{suffix}"
    if key not in data:
        available = "\n".join(sorted(data.files))
        raise KeyError(
            f"Missing key '{key}' in npz. Available keys:\n{available}"
        )
    return np.asarray(data[key])


def compute_limits(arrays, padding_ratio=0.06):
    """
    Compute rectangular axis limits from a list of arrays.

    - inputs:
        - List of coordinate arrays and padding ratio.
    - outputs:
        - (xlim, ylim) tuples with padding.
    - process:
        - Stack arrays, measure min/max spans, apply minimum span guard and padding.
    """
    all_y = np.vstack(arrays)
    xmin, ymin = np.min(all_y, axis=0)
    xmax, ymax = np.max(all_y, axis=0)

    xspan = xmax - xmin
    yspan = ymax - ymin

    if xspan < 1e-12:
        xspan = 1e-12
    if yspan < 1e-12:
        yspan = 1e-12

    xpad = xspan * padding_ratio
    ypad = yspan * padding_ratio

    return (xmin - xpad, xmax + xpad), (ymin - ypad, ymax + ypad)


def compute_equal_limits(arrays, padding_ratio=0.06):
    """
    Compute equal-aspect axis limits covering all arrays.

    - inputs:
        - List of coordinate arrays and padding ratio.
    - outputs:
        - (xlim, ylim) tuples with shared half-span.
    - process:
        - Stack arrays, find global center and max span, and build square limits.
    """
    all_y = np.vstack(arrays)
    xmin, ymin = np.min(all_y, axis=0)
    xmax, ymax = np.max(all_y, axis=0)

    xspan = xmax - xmin
    yspan = ymax - ymin
    span = max(xspan, yspan, 1e-12)

    xmid = 0.5 * (xmin + xmax)
    ymid = 0.5 * (ymin + ymax)

    half = 0.5 * span * (1.0 + 2.0 * padding_ratio)
    return (xmid - half, xmid + half), (ymid - half, ymid + half)


def limits_for_arrays(arrays, padding_ratio, equal):
    """
    Select standard or equal-aspect limit computation.

    - inputs:
        - Arrays list, padding ratio, and equal-limit flag.
    - outputs:
        - (xlim, ylim) tuples.
    - process:
        - Dispatch to equal or standard limit helper.
    """
    if equal:
        return compute_equal_limits(arrays, padding_ratio=padding_ratio)
    return compute_limits(arrays, padding_ratio=padding_ratio)


def unique_labels(labels):
    """
    Return deterministically ordered unique labels.

    - inputs:
        - Label array.
    - outputs:
        - Sorted unique labels.
    - process:
        - Try numeric sort first; fallback to string sort on conversion failure.
    """
    try:
        return sorted(np.unique(labels), key=lambda x: float(x))
    except Exception:
        return sorted(np.unique(labels), key=lambda x: str(x))


def plot_grid(
    arrays_by_momentum,
    labels,
    momentums,
    stage,
    scale_mode,
    output_pdf,
    args,
):
    """
    Render and save a 2x3 grid for one stage.

    - inputs:
        - Momentum-indexed arrays, labels, momentum list, stage, scale mode,
          output paths, and plotting args.
    - outputs:
        - Saved PDF figure file.
    - process:
        - Compute limits, draw per-panel scatter plots, apply legends/titles, and save.
    """
    fig, axes = plt.subplots(2, 3, figsize=tuple(args.figure_size), constrained_layout=True)
    axes = axes.ravel()

    uniq = unique_labels(labels)

    if scale_mode == "global":
        global_xlim, global_ylim = limits_for_arrays(
            [arrays_by_momentum[m] for m in momentums],
            padding_ratio=args.limit_padding_ratio,
            equal=args.equal_each_panel,
        )
    else:
        global_xlim, global_ylim = None, None

    scatter_handles = {}
    for ax_idx, (ax, momentum) in enumerate(zip(axes, momentums)):
        Y = arrays_by_momentum[momentum]

        for lab in uniq:
            mask = labels == lab
            sc = ax.scatter(
                Y[mask, 0],
                Y[mask, 1],
                s=args.point_size,
                alpha=args.alpha,
                label=str(lab),
            )
            if lab not in scatter_handles:
                scatter_handles[lab] = sc

        ax.set_title(panel_title(momentum, stage), fontsize=14)
        ax.tick_params(labelsize=8)
        ax.grid(False)

        if scale_mode == "panel":
            xlim, ylim = limits_for_arrays(
                [Y],
                padding_ratio=args.limit_padding_ratio,
                equal=args.equal_each_panel,
            )
            ax.set_xlim(*xlim)
            ax.set_ylim(*ylim)
        else:
            ax.set_xlim(*global_xlim)
            ax.set_ylim(*global_ylim)

        if args.equal_each_panel:
            ax.set_aspect("equal", adjustable="box")

        if args.legend == "last" and ax_idx == len(momentums) - 1:
            ax.legend(title="label", fontsize=7, title_fontsize=8, loc="best")

    for ax in axes[len(momentums):]:
        ax.axis("off")

    if args.legend == "outside":
        handles = [scatter_handles[lab] for lab in uniq]
        names = [str(lab) for lab in uniq]
        fig.legend(
            handles,
            names,
            title="label",
            loc="center right",
            fontsize=8,
            title_fontsize=9,
        )

    if stage == "ee":
        fig.suptitle("EE stage end: discrete MM momentum sweep", fontsize=20)
    else:
        fig.suptitle("Common original t-SNE embedding stage after EE momentum sweep", fontsize=20)

    fig.savefig(output_pdf, bbox_inches="tight")
    plt.close(fig)


def main():
    """
    Run plotting pipeline for EE and final stages.

    - inputs:
        - CLI arguments from the current process.
    - outputs:
        - Saved stage figures and printed output paths.
    - process:
        - Load npz, resolve momentums, gather arrays, render two grids, and save files.
    """
    args = parse_args()
    npz_path = Path(args.npz)
    output_dir = Path(args.output_dir) if args.output_dir else npz_path.parent
    output_dir.mkdir(parents=True, exist_ok=True)

    data = np.load(npz_path, allow_pickle=True)
    labels = np.asarray(data["labels"])
    momentums = get_momentums(data, args.momentums)

    ee_arrays = {}
    final_arrays = {}
    for m in momentums:
        ee_arrays[m] = get_array_for_momentum(data, m, "state")
        final_arrays[m] = get_array_for_momentum(data, m, "embedding_final")

    scale_suffix = f"ee_{args.ee_scale}_embedding_{args.embedding_scale}"

    ee_pdf = output_dir / f"ee_stage_end_discrete_mm_momentum_sweep_{args.ee_scale}_scale.pdf"
    final_pdf = output_dir / f"embedding_stage_end_discrete_mm_momentum_sweep_{args.embedding_scale}_scale.pdf"

    plot_grid(
        arrays_by_momentum=ee_arrays,
        labels=labels,
        momentums=momentums,
        stage="ee",
        scale_mode=args.ee_scale,
        output_pdf=ee_pdf,
        args=args,
    )
    plot_grid(
        arrays_by_momentum=final_arrays,
        labels=labels,
        momentums=momentums,
        stage="embedding",
        scale_mode=args.embedding_scale,
        output_pdf=final_pdf,
        args=args,
    )

    print("Saved:")
    print(" ", ee_pdf)
    print(" ", final_pdf)


if __name__ == "__main__":
    main()

#!/usr/bin/env python3
"""
Evaluate how ORC and SRC differ as cycle rank increases.

Inputs:
  - manifest.csv and graph_*.graphml files generated by generate_cycle_controlled_graphs.py.

Outputs:
  - orc_src_edge_curvatures.csv
  - orc_src_gap_summary.csv

Recommended comparison:
  --edge-set tree
This compares ORC and SRC on the original tree backbone edges while ORC is computed
on G_eta, whose cycle rank increases with eta.

This version also reports how many edges are discarded by the SRC tree projection.

Important:
  SRCFlowMethod.stats["u_idx"], ["v_idx"] may contain the graph edges on which
  curvature values are reported, not necessarily the selected MST/SPT tree edges.
  Therefore, this script explicitly reconstructs the SRC tree edge set from
  --src-method:
    - src-mst: minimum spanning tree of G_eta using length
    - src-spt: shortest path tree rooted at --src-root using length
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

import networkx as nx
import numpy as np
import pandas as pd


def find_project_root(start: Path) -> Path:
    cur = start.resolve()
    for candidate in [cur] + list(cur.parents):
        if (candidate / "src").is_dir():
            return candidate
    raise RuntimeError(
        "Could not find project root containing ./src. "
        "Run this script from inside the project, or place it under project_root/experiments/..."
    )


PROJECT_ROOT = find_project_root(Path(__file__).resolve().parent)
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.GraphRicciCurvature.OllivierRicci import OllivierRicci
from src.flow import SRCFlowMethod


OPTIMIZATION_EDGE_ATTR = "length"


def parse_args():
    parser = argparse.ArgumentParser(description="Evaluate ORC/SRC gap on cycle-controlled graphs.")
    parser.add_argument("--graph-dir", default="outputs/cycle_controlled_graphs")
    parser.add_argument("--manifest", default=None)
    parser.add_argument("--output-dir", default=None)

    parser.add_argument("--edge-set", choices=["tree", "all", "extra"], default="tree")
    parser.add_argument("--src-method", choices=["src-mst", "src-spt"], default="src-mst")
    parser.add_argument("--src-root", type=int, default=0)
    parser.add_argument("--src-delta", type=float, default=0.5)
    parser.add_argument("--src-p", type=float, default=1.0)

    parser.add_argument("--orc-alpha", type=float, default=0.5)
    parser.add_argument("--orc-method", default="OTDSinkhornMix")
    parser.add_argument("--orc-proc", type=int, default=1)
    parser.add_argument("--orc-shortest-path", choices=["all_pairs", "pairwise"], default="all_pairs")
    parser.add_argument("--orc-verbose", default="ERROR")

    parser.add_argument("--max-graphs", type=int, default=None)
    return parser.parse_args()


def edge_key(u, v):
    u, v = int(u), int(v)
    return (u, v) if u <= v else (v, u)


def parse_boolish(x):
    if isinstance(x, (int, np.integer)):
        return int(x) != 0
    if isinstance(x, (float, np.floating)):
        return int(x) != 0
    if isinstance(x, str):
        return x.strip().lower() in {"1", "true", "yes"}
    return bool(x)


def load_graph(path: Path) -> nx.Graph:
    G_raw = nx.read_graphml(path)

    mapping = {n: int(n) for n in G_raw.nodes()}
    G_raw = nx.relabel_nodes(G_raw, mapping)

    G = nx.Graph()
    for n, d in G_raw.nodes(data=True):
        attrs = {}
        for k, v in d.items():
            try:
                attrs[k] = float(v)
            except Exception:
                attrs[k] = v
        G.add_node(int(n), **attrs)

    for u, v, d in G_raw.edges(data=True):
        attrs = {}
        for k, val in d.items():
            if k in {"length", "weight"}:
                attrs[k] = float(val)
            elif k in {"is_tree", "is_extra"}:
                attrs[k] = int(float(val))
            else:
                try:
                    attrs[k] = float(val)
                except Exception:
                    attrs[k] = val
        if "length" not in attrs:
            attrs["length"] = float(attrs.get("weight", 1.0))
        if "weight" not in attrs:
            attrs["weight"] = float(attrs["length"])
        G.add_edge(int(u), int(v), **attrs)

    return G


def graph_edge_keys(G: nx.Graph) -> set[tuple[int, int]]:
    return {edge_key(u, v) for u, v in G.edges()}


def tree_and_extra_edge_sets(G: nx.Graph) -> tuple[set[tuple[int, int]], set[tuple[int, int]]]:
    base_tree_edges = set()
    extra_edges = set()
    for u, v, d in G.edges(data=True):
        e = edge_key(u, v)
        if parse_boolish(d.get("is_tree", 0)):
            base_tree_edges.add(e)
        if parse_boolish(d.get("is_extra", 0)):
            extra_edges.add(e)
    return base_tree_edges, extra_edges


def src_projection_tree_edges(G: nx.Graph, args) -> set[tuple[int, int]]:
    """Explicitly reconstruct the tree used by the SRC projection.

    This is used only for edge-selection accounting. Curvature values are still
    taken from SRCFlowMethod, so this does not change the ORC/SRC gap values.
    """
    H = G.copy()
    for u, v, d in H.edges(data=True):
        d["length"] = float(d.get("length", d.get("weight", 1.0)))

    if args.src_method == "src-mst":
        T = nx.minimum_spanning_tree(H, weight="length")
        return graph_edge_keys(T)

    if args.src_method == "src-spt":
        root = int(args.src_root)
        if root not in H:
            raise ValueError(f"src_root={root} is not a node in the graph.")

        # For a connected graph, this gives paths to all nodes. If a graph is
        # unexpectedly disconnected, it returns the reachable component only.
        paths = nx.single_source_dijkstra_path(H, source=root, weight="length")

        edges = set()
        for node, path in paths.items():
            if int(node) == root:
                continue
            if len(path) < 2:
                continue
            u = int(path[-2])
            v = int(path[-1])
            edges.add(edge_key(u, v))
        return edges

    raise ValueError(f"Unknown src_method: {args.src_method}")


def compute_edge_selection_counts(
    G: nx.Graph,
    src_tree_edges: set[tuple[int, int]],
) -> dict:
    all_edges = graph_edge_keys(G)
    base_tree_edges, extra_edges = tree_and_extra_edge_sets(G)

    src_tree_edges = set(src_tree_edges) & all_edges
    cut_edges = all_edges - src_tree_edges

    known_labeled_edges = base_tree_edges | extra_edges
    unlabeled_edges = all_edges - known_labeled_edges

    kept_base_tree_edges = base_tree_edges & src_tree_edges
    cut_base_tree_edges = base_tree_edges - src_tree_edges

    kept_added_edges = extra_edges & src_tree_edges
    cut_added_edges = extra_edges - src_tree_edges

    kept_unlabeled_edges = unlabeled_edges & src_tree_edges
    cut_unlabeled_edges = unlabeled_edges - src_tree_edges

    expected_forest_edges = G.number_of_nodes() - nx.number_connected_components(G)

    return {
        "n_edges_graph": int(len(all_edges)),
        "n_edges_src_tree": int(len(src_tree_edges)),
        "n_edges_base_tree": int(len(base_tree_edges)),
        "n_edges_extra": int(len(extra_edges)),
        "n_edges_unlabeled": int(len(unlabeled_edges)),

        "cut_edges_total": int(len(cut_edges)),

        "kept_base_tree_edges": int(len(kept_base_tree_edges)),
        "cut_base_tree_edges": int(len(cut_base_tree_edges)),

        "kept_added_edges": int(len(kept_added_edges)),
        "cut_added_edges": int(len(cut_added_edges)),

        "kept_unlabeled_edges": int(len(kept_unlabeled_edges)),
        "cut_unlabeled_edges": int(len(cut_unlabeled_edges)),

        # Sanity checks. These should be 0 for the expected setup.
        "cut_edges_accounting_error": int(
            len(cut_edges)
            - (len(cut_base_tree_edges) + len(cut_added_edges) + len(cut_unlabeled_edges))
        ),
        "tree_size_error": int(len(src_tree_edges) - expected_forest_edges),
    }


def compute_orc_edge_curvature(G: nx.Graph, args) -> dict[tuple[int, int], float]:
    H = G.copy()
    for u, v, d in H.edges(data=True):
        d["weight"] = float(d.get("length", d.get("weight", 1.0)))

    orc = OllivierRicci(
        H,
        weight="weight",
        alpha=args.orc_alpha,
        method=args.orc_method,
        shortest_path=args.orc_shortest_path,
        proc=args.orc_proc,
        verbose=args.orc_verbose,
    )
    orc.compute_ricci_curvature()

    out = {}
    for u, v, d in orc.G.edges(data=True):
        out[edge_key(u, v)] = float(d.get("ricciCurvature", np.nan))
    return out


def compute_src_edge_curvature(G: nx.Graph, args) -> dict[tuple[int, int], float]:
    H = G.copy()
    for u, v, d in H.edges(data=True):
        d["length"] = float(d.get("length", d.get("weight", 1.0)))

    src = SRCFlowMethod(
        delta=args.src_delta,
        steps=1,
        p=args.src_p,
        method=args.src_method,
        src_root=args.src_root,
    )
    _, stats = src.run(H)

    kappas = np.asarray(stats["kappa_hist"][0], dtype=float)
    u_idx = np.asarray(stats["u_idx"], dtype=int)
    v_idx = np.asarray(stats["v_idx"], dtype=int)
    nodes = list(stats["nodes"])

    out = {}
    for i, kappa in enumerate(kappas):
        u = int(nodes[int(u_idx[i])])
        v = int(nodes[int(v_idx[i])])
        out[edge_key(u, v)] = float(kappa)

    return out


def selected_edges(G: nx.Graph, edge_set: str):
    edges = []
    for u, v, d in G.edges(data=True):
        is_tree = parse_boolish(d.get("is_tree", 0))
        is_extra = parse_boolish(d.get("is_extra", 0))
        if edge_set == "tree" and not is_tree:
            continue
        if edge_set == "extra" and not is_extra:
            continue
        edges.append((int(u), int(v), d))
    return edges


def safe_corr(x, y, kind="pearson"):
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    mask = np.isfinite(x) & np.isfinite(y)
    x = x[mask]
    y = y[mask]
    if len(x) < 3:
        return np.nan
    if np.std(x) == 0 or np.std(y) == 0:
        return np.nan
    if kind == "pearson":
        return float(np.corrcoef(x, y)[0, 1])
    if kind == "spearman":
        rx = pd.Series(x).rank(method="average").to_numpy()
        ry = pd.Series(y).rank(method="average").to_numpy()
        if np.std(rx) == 0 or np.std(ry) == 0:
            return np.nan
        return float(np.corrcoef(rx, ry)[0, 1])
    raise ValueError(kind)


def summarize_edges(df: pd.DataFrame) -> dict:
    if df.empty:
        return {
            "n_compared_edges": 0,
            "mean_abs_gap": np.nan,
            "median_abs_gap": np.nan,
            "rmse_gap": np.nan,
            "pearson_orc_src": np.nan,
            "spearman_orc_src": np.nan,
            "sign_mismatch_rate": np.nan,
            "mean_orc": np.nan,
            "mean_src": np.nan,
        }

    gap = df["orc"] - df["src"]
    sign_orc = np.sign(df["orc"].to_numpy())
    sign_src = np.sign(df["src"].to_numpy())
    sign_mismatch = sign_orc != sign_src

    return {
        "n_compared_edges": int(len(df)),
        "mean_abs_gap": float(np.mean(np.abs(gap))),
        "median_abs_gap": float(np.median(np.abs(gap))),
        "rmse_gap": float(np.sqrt(np.mean(gap ** 2))),
        "pearson_orc_src": safe_corr(df["orc"], df["src"], "pearson"),
        "spearman_orc_src": safe_corr(df["orc"], df["src"], "spearman"),
        "sign_mismatch_rate": float(np.mean(sign_mismatch)),
        "mean_orc": float(df["orc"].mean()),
        "mean_src": float(df["src"].mean()),
    }


def main():
    args = parse_args()
    graph_dir = Path(args.graph_dir)
    manifest_path = Path(args.manifest) if args.manifest else graph_dir / "manifest.csv"
    output_dir = Path(args.output_dir) if args.output_dir else graph_dir / "orc_src_gap"
    output_dir.mkdir(parents=True, exist_ok=True)

    manifest = pd.read_csv(manifest_path)
    if args.max_graphs is not None:
        manifest = manifest.head(int(args.max_graphs))

    edge_rows = []
    summary_rows = []

    for row_idx, row in manifest.iterrows():
        graph_path = graph_dir / str(row["graph_path"])
        print(f"[{row_idx + 1}/{len(manifest)}] {graph_path}", flush=True)

        G = load_graph(graph_path)
        beta1 = int(row.get("beta1", G.number_of_edges() - G.number_of_nodes() + nx.number_connected_components(G)))
        eta_actual = float(row.get("eta_actual", np.nan))

        # Curvature values.
        orc_curv = compute_orc_edge_curvature(G, args)
        src_curv = compute_src_edge_curvature(G, args)

        # Edge-selection accounting for the SRC tree projection.
        src_tree_edges = src_projection_tree_edges(G, args)
        selection_counts = compute_edge_selection_counts(G, src_tree_edges)

        local_rows = []
        for u, v, d in selected_edges(G, args.edge_set):
            e = edge_key(u, v)
            if e not in orc_curv or e not in src_curv:
                continue
            orc_val = float(orc_curv[e])
            src_val = float(src_curv[e])
            local_rows.append({
                "eta_actual": eta_actual,
                "eta_requested": float(row.get("eta_requested", eta_actual)),
                "beta1": beta1,
                "m_add": int(row.get("m_add", beta1)),
                "edge_set": args.edge_set,
                "u": e[0],
                "v": e[1],
                "length": float(d.get("length", np.nan)),
                "is_tree": int(parse_boolish(d.get("is_tree", 0))),
                "is_extra": int(parse_boolish(d.get("is_extra", 0))),
                "is_src_tree": int(e in src_tree_edges),
                "orc": orc_val,
                "src": src_val,
                "abs_gap": abs(orc_val - src_val),
                "sign_mismatch": int(np.sign(orc_val) != np.sign(src_val)),
            })

        local_df = pd.DataFrame(local_rows)
        edge_rows.extend(local_rows)

        summary = {
            "eta_actual": eta_actual,
            "eta_requested": float(row.get("eta_requested", eta_actual)),
            "beta1": beta1,
            "m_add": int(row.get("m_add", beta1)),
            "n_nodes": int(G.number_of_nodes()),

            # Backward-compatible old column.
            "n_edges": int(G.number_of_edges()),

            "edge_set": args.edge_set,
            "src_method": args.src_method,
            "src_root": args.src_root,
            "src_delta": args.src_delta,
            "src_p": args.src_p,
            "orc_alpha": args.orc_alpha,
        }
        summary.update(selection_counts)
        summary.update(summarize_edges(local_df))
        summary_rows.append(summary)

    edge_df = pd.DataFrame(edge_rows)
    summary_df = pd.DataFrame(summary_rows)

    edge_path = output_dir / "orc_src_edge_curvatures.csv"
    summary_path = output_dir / "orc_src_gap_summary.csv"

    edge_df.to_csv(edge_path, index=False)
    summary_df.to_csv(summary_path, index=False)

    print("Saved:")
    print(" ", edge_path)
    print(" ", summary_path)


if __name__ == "__main__":
    main()
